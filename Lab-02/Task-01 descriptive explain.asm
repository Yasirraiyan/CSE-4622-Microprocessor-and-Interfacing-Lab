ORG 0100h              ; প্রোগ্রামের শুরু হচ্ছে 0100h মেমোরি অ্যাড্রেস থেকে (.COM ফাইলের জন্য স্ট্যান্ডার্ড শুরু)

MAIN PROC              ; MAIN নামের প্রোগ্রাম সেগমেন্ট শুরু

    ;------------------------------
    ; ইউজার থেকে একটি ক্যারেক্টার ইনপুট নেওয়া হবে
    ;------------------------------
    MOV AH, 1           ; AH = 1 → DOS interrupt 21h এর function 1 ব্যবহার করা হবে (সিঙ্গেল ক্যারেক্টার ইনপুট)
    INT 21h             ; কীবোর্ড থেকে ইনপুট নেওয়া হবে, AL রেজিস্টারে ক্যারেক্টার আসবে
    MOV BL, AL          ; ইনপুট AL থেকে BL রেজিস্টারে কপি করা হচ্ছে (পরে ব্যবহার করব)

    ;------------------------------
    ; একটি নতুন লাইন দেখানো হচ্ছে (Carriage Return + Line Feed)
    ;------------------------------
    MOV AH, 2           ; AH = 2 → DOS interrupt 21h function 2 (সিঙ্গেল ক্যারেক্টার প্রিন্ট)
    MOV DL, 0Dh         ; Carriage Return (কার্সর লাইনের শুরুতে যাবে)
    INT 21h             ; স্ক্রিনে carriage return দেখানো হচ্ছে

    MOV DL, 0Ah         ; Line Feed (কার্সর নিচের লাইনে যাবে)
    INT 21h             ; স্ক্রিনে লাইন ফিড দেখানো হচ্ছে

    ;------------------------------
    ; ASCII value থেকে EVEN বা ODD চেক করা হচ্ছে
    ;------------------------------
    AND BL, 1           ; LSB চেক করা হচ্ছে → যদি 0 হয় তবে EVEN, 1 হলে ODD
    JZ EVEN             ; যদি result 0 হয়, তাহলে EVEN লেবেলে চলে যাও

ODD:                   ; যদি EVEN না হয়, তাহলে এখানে আসবে
    MOV AH, 2           ; ক্যারেক্টার প্রিন্ট ফাংশন
    MOV DL, 'O'         ; 'O' ক্যারেক্টার (ODD বোঝাতে)
    INT 21h             ; স্ক্রিনে 'O' দেখানো হচ্ছে
    JMP END_PROGRAM     ; EVEN অংশ স্কিপ করে প্রোগ্রামের শেষ অংশে চলে যাও

EVEN:                  ; যদি সংখ্যা EVEN হয় তাহলে এখানে আসবে
    MOV AH, 2           ; ক্যারেক্টার প্রিন্ট ফাংশন
    MOV DL, 'E'         ; 'E' ক্যারেক্টার (EVEN বোঝাতে)
    INT 21h             ; স্ক্রিনে 'E' দেখানো হচ্ছে

END_PROGRAM:           ; প্রোগ্রামের শেষ অংশ
    MOV AH, 4Ch         ; DOS interrupt 21h → function 4Ch → প্রোগ্রাম টার্মিনেট করার জন্য
    INT 21h             ; প্রোগ্রাম বন্ধ করে DOS-এ ফিরে যায়

MAIN ENDP              ; MAIN প্রোগ্রামের শেষ

END MAIN               ; প্রোগ্রামের এন্ট্রি পয়েন্ট MAIN
